
public class ConversaoDeUnidadesDeVolum {

	public static double litrosParaCentimetrosCubicos(double litro) {
		return litro * 1000;
			  }
	
	public static double metroCubicoParalitros(double metroCubico) {
		return metroCubico * 1000;
			  }
	
	public static double metroCubicoPesCubicos(double metroCubicos) {
		return metroCubicos* 35.32 ;
			  }
	public static double galaoAmericanoParaPolegadasCubicas (double galaoAmericano ) {
		return   galaoAmericano * 231 ;
			  }
	public static double galaoAmericanoParaLitros (double galaoAmericano ) {
		return   galaoAmericano * 3.785 ;
			  }
	public static double centimetrosCubicosParaLitros(double centimetrosCubicos) {
		return centimetrosCubicos /1000;
			  }
	public static double metroCubicoParacentimetrosCubicos(double metroCubico) {
		
		return metroCubico *1000000;
			  }
	
}
